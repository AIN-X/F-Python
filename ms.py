#              Python - Modify Strings

print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Upper Case <<<<<<<<<<<<<<<<<<<<<<<<<\n')

a = 'Hello world'
print(a.upper())

print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Lower Case <<<<<<<<<<<<<<<<<<<<<<<<<\n')

a = 'Hello world'
print (a.lower())

print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Remove Whitespace <<<<<<<<<<<<<<<<<<<<<<<<<\n')

a = 'Hello world'
print(a.strip())


print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Replace String <<<<<<<<<<<<<<<<<<<<<<<<<\n')

a = 'Hello world'
print(a.replace("H", "J" ))


print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Split String <<<<<<<<<<<<<<<<<<<<<<<<<\n')

a = 'Hello world'
print(a.split(","), "\n")
