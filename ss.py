# Python - Slicing Strings

print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Slicing <<<<<<<<<<<<<<<<<<<<<<<<<\n')
b = 'Naeem Islam '

print(b[2:5])

print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Slice From the Start <<<<<<<<<<<<<<<<<<<<<<<<<\n')

b = 'Naeem Islam '
print(b[:6])

print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Slice To the End <<<<<<<<<<<<<<<<<<<<<<<<<\n')

b = 'Naeem Islam '
print(b[1:])

print('\n>>>>>>>>>>>>>>>>>>>>>>>>>> Negative Indexing <<<<<<<<<<<<<<<<<<<<<<<<<\n')

b = 'Naeem Islam '
print(b[-5:-2])